package com.nsc.test.model;

public class Member {
	private String member_id=null;
	private String member_pwd=null;
	private String member_nm=null;
	private String member_enroll_dt=null;
	
	public String getMember_id() {
		return member_id;
	}
	public void setMember_id(String member_id) {
		this.member_id = member_id;
	}
	public String getMember_pwd() {
		return member_pwd;
	}
	public void setMember_pwd(String member_pwd) {
		this.member_pwd = member_pwd;
	}
	public String getMember_nm() {
		return member_nm;
	}
	public void setMember_nm(String member_nm) {
		this.member_nm = member_nm;
	}
	public String getMember_enroll_dt() {
		return member_enroll_dt;
	}
	public void setMember_enroll_dt(String member_enroll_dt) {
		this.member_enroll_dt = member_enroll_dt;
	}

}
